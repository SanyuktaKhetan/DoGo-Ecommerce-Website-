//Middleware functions can perform a wide range of tasks,
//such as logging, authentication, error handling, and more,
//before passing control to the next middleware function or route handler.

import JWT from "jsonwebtoken";
import userModel from "../models/userModel.js";

//protected routes --> token based

// when we get the req then next is validated and then respond is send
//with the help of next we can validate the used
export const requireSignIn = async (req, res, next) => {
  try {
    const decode = JWT.verify(
      req.headers.authorization,
      process.env.JWT_SECRET
    );
    req.user = decode;
    next();
  } catch (error) {
    console.log(error);
  }
};

//admin access
export const isAdmin = async (req, res, next) => {
  try {
    const user = await userModel.findById(req.user._id);
    if (user.role !== 1) {
      return res.status(401).send({
        success: false,
        message: " Unauthorized Access",
      });
    } else {
      next();
    }
  } catch (error) {
    console.log(error);
    res.status(401).send({
      success: false,
      message: "Error in admin middleware",
      error,
    });
  }
};
